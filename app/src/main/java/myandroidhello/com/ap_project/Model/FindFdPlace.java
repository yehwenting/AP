public class FindFdPlace {
    private String name;
    private String place;


    public FindFdPlace(String name, String place) {
        this.name = this.name;
        this.place = this.place;

    }

    public String getName() {
        return name;
    }

    public String getPlace() {
        return place;
    }


    public void setName(String name) {
        this.name = name;
    }

    public void setPlace(String place) {
        this.place = place;
    }


}
